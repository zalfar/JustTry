package com.example.justtry;

public class ModelChart {
    int age, measure;

    public ModelChart(){}

    public ModelChart(int age, int measure) {
        this.age = age;
        this.measure = measure;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getMeasure() {
        return measure;
    }

    public void setMeasure(int measure) {
        this.measure = measure;
    }
}
